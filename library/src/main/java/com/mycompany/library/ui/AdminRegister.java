    private void initializeUI() {

        setTitle("註冊");
        setLocationRelativeTo(null);
        
        jButton1.addActionListener((ActionEvent e) -> {
//            openIndexFrame(adminId);
            SwingUtilities.invokeLater(() -> {
            new AdminLogin().setVisible(true);
        });
        this.dispose(); // 关闭登录窗口
        });
        
        registBtn.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                AdminRepository AdminRepository = new AdminRepository();
                
                String id = userId.getText();
                String a_name = name.getText();
                String a_phone = phone.getText();
                String a_sex = man.isSelected() ? "男" : "女";
                String a_passwd = jPasswordField2.getText();
//                String a_note = man.isSelected();
                
                if (!id.isEmpty() && !a_name.isEmpty() && !a_phone.isEmpty() && !a_sex.isEmpty() && !a_passwd.isEmpty()) {
                    if (AdminRepository.isAdminIdExists(id)) {
                        // 显示错误弹窗，ID 已存在
                        JOptionPane.showMessageDialog(AdminRegister.this, "此ID已註冊過", "錯誤", JOptionPane.ERROR_MESSAGE);
                    } else {
                        // 插入用户数据到数据库
                        AdminRepository.insert(id,a_name,a_phone,a_sex,a_passwd);
                        
                        // 显示成功弹窗
                        JOptionPane.showMessageDialog(AdminRegister.this, "註冊成功！", "成功", JOptionPane.INFORMATION_MESSAGE);
                        openLoginFrame();
                    }
                } else {
                    JOptionPane.showMessageDialog(AdminRegister.this, "請完整填寫所有欄位", "錯誤", JOptionPane.ERROR_MESSAGE);
                }
            }
        });
    }

