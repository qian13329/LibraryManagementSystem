    private void initializeUI() {
        setTitle("驗證身分");
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setLocationRelativeTo(null);
        
        jButton2.addActionListener((ActionEvent e) -> {
            openIndexFrame(adminId);
        });
        
        jButton1.addActionListener((ActionEvent e) -> {
            String userId = jTextField1.getText();
            if (!userId.isEmpty()) {
                if (isValidUserId(userId)) { // 檢查使用者ID是否有效
                    if(checkFine(userId)>0){
                      JOptionPane.showMessageDialog(CheckReturn.this, "尚有欠費，請先支付罰緩");
                      openFineFrame(userId,adminId);
                    }
                    else{
                        JOptionPane.showMessageDialog(CheckReturn.this, "驗證成功");
                        openReturnFrame(userId,adminId);
                    }
                } else {
                    jTextField1.setText("");
                    JOptionPane.showMessageDialog(CheckReturn.this, "ID輸入錯誤，請重試！", "錯誤", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(CheckReturn.this, "請輸入ID", "錯誤", JOptionPane.ERROR_MESSAGE);
            }
        });
        }
    
          // 判斷ID是否存在
    private boolean isValidUserId(String id) {
        UserRepository UserRepository = new UserRepository();
        return UserRepository.isUserIdExists(id);
    }
    //判斷罰金
    private int checkFine(String id) {
        BorrowRecordRepository BorrowRecordRepository = new BorrowRecordRepository();
        BorrowRecordRepository.calculateFines();
        int fine=BorrowRecordRepository.getUserFine(id);
        return fine;
    }
    
    private void openFineFrame(String userId,String adminId) {
        SwingUtilities.invokeLater(() -> {
            new Fine(userId,adminId).setVisible(true);
            
        });
        this.dispose(); // 关闭登录窗口
    }
    
    private void openReturnFrame(String userId,String adminId) {
        SwingUtilities.invokeLater(() -> {
            new AdminReturnBook(userId,adminId).setVisible(true);
            
        });
        this.dispose(); // 关闭登录窗口
    }
    
     private void openIndexFrame(String adminId) {
        SwingUtilities.invokeLater(() -> {
            new AdminIndex(adminId).setVisible(true);
            
        });
        this.dispose(); // 关闭登录窗口
    }
