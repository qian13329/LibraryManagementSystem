    private void initializeUI() {

        setTitle("管理員登入");
        setLocationRelativeTo(null);

        loginBtn.addActionListener((ActionEvent e) -> {
            String id = userId.getText();
            String passwd = jPasswordField1.getText();
            if (!id.isEmpty()) {
                if (isValidUserId(id)) { // 檢查使用者ID是否有效
                    if(isValidPasswd(id,passwd))
                        openIndexFrame(id);
                     else {
                        JOptionPane.showMessageDialog(AdminLogin.this, "登入失敗，請重試！");
                    }
                } else {
                    userId.setText("");
                    JOptionPane.showMessageDialog(AdminLogin.this, "無效ID，請重試！", "錯誤", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(AdminLogin.this, "請輸入ID", "錯誤", JOptionPane.ERROR_MESSAGE);
            }
        });
    }
//    
    private void openIndexFrame(String userId) {
        SwingUtilities.invokeLater(() -> {
            new AdminIndex(userId).setVisible(true);
        });
        this.dispose(); // 关闭登录窗口
    }
    
    private void openRegisterFrame() {
        SwingUtilities.invokeLater(() -> {
            new AdminRegister().setVisible(true);
        });
        this.dispose(); // 关闭登录窗口
    }
    
    // 判斷ID是否存在
    private boolean isValidUserId(String id) {
        AdminRepository AdminRepository = new AdminRepository();
        return AdminRepository.isAdminIdExists(id);
    }
    //驗證密碼
    private boolean isValidPasswd(String id,String passwd) {
        AdminRepository AdminRepository = new AdminRepository();
        return AdminRepository.validate(id,passwd);
    }
    

