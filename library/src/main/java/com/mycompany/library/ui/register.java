    private void initializeUI() {

        setTitle("註冊");
        setLocationRelativeTo(null);

        //跳login
        jButton1.addActionListener((ActionEvent e) -> {
            SwingUtilities.invokeLater(() -> {
            new userLogin().setVisible(true);
        });
        this.dispose(); // 关闭登录窗口
        });
        
        registBtn.addActionListener((ActionEvent e) -> {
            UserRepository userRepository = new UserRepository();
            
            String id = userId.getText();
            String userName = name.getText();
            String userPhone = phone.getText();
            String userSex = man.isSelected() ? "男" : "女";
            
            if (!id.isEmpty() && !userName.isEmpty() && !userPhone.isEmpty() && !userSex.isEmpty()) {
                if (userRepository.isUserIdExists(id)) {
                    // 显示错误弹窗，ID 已存在
                    JOptionPane.showMessageDialog(register.this, "此ID已註冊過", "錯誤", JOptionPane.ERROR_MESSAGE);
                } else {
                    // 插入用户数据到数据库
                    userRepository.insertUser(id, userName, userPhone, userSex);

                    // 显示成功弹窗
                    JOptionPane.showMessageDialog(register.this, "註冊成功！", "成功", JOptionPane.INFORMATION_MESSAGE);
                    openLoginFrame();
                }
            } else {
                JOptionPane.showMessageDialog(register.this, "請完整填寫所有欄位", "錯誤", JOptionPane.ERROR_MESSAGE);
            }
        });
    }

