    private void addBook() {
        Long id = Long.valueOf(new Random().nextInt(9000000) + 10000);
        String title = jTextField1.getText().trim();
        String author = jTextField2.getText().trim();
        String isbn = jTextField3.getText().trim();
        String subject = jTextField4.getText().trim();
        Boolean borrowed = false;

        if (!title.isEmpty()) {
            Book newBook = new Book();
            newBook.setTitle(title);
            newBook.setAuthor(author);
            newBook.setIsbn(isbn);
            newBook.setSubject(subject);
            newBook.setBorrowed(borrowed);
            newBook.setId(id);

            bookRepository.add(newBook);

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");

            Object[] row = {newBook.getId(), newBook.getTitle(), newBook.getAuthor(), newBook.getIsbn(), newBook.getSubject(), newBook.isBorrowed()};
            tableModel.addRow(row);
        } else {
            JOptionPane.showMessageDialog(this, "請輸入完整的書籍信息", "錯誤", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void populateTable(List<Book> books) {
        for (Book book : books) {
            Object[] row = {book.getId(), book.getTitle(), book.getAuthor(), book.getIsbn(), book.getSubject(), book.isBorrowed()};
            tableModel.addRow(row);
        }
    }
    
