    private void initializeUI() {

        setTitle("登入");
        setLocationRelativeTo(null);

        loginBtn.addActionListener((ActionEvent e) -> {
            String id = userId.getText();
            if (!id.isEmpty()) {
                if (isValidUserId(id)) { // 檢查使用者ID是否有效
                    openIndexFrame(id);
                } else {
                    userId.setText("");
                    JOptionPane.showMessageDialog(userLogin.this, "ID輸入錯誤，請重試！", "錯誤", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(userLogin.this, "請輸入ID", "錯誤", JOptionPane.ERROR_MESSAGE);
            }
        });
        
        regisBtn.addActionListener((ActionEvent e) -> {
            openRegisterFrame();
        });
    }
    
    private void openIndexFrame(String userId) {
        SwingUtilities.invokeLater(() -> {
            new index(userId).setVisible(true);
        });
        this.dispose(); // 关闭登录窗口
    }
    private void openRegisterFrame() {
        SwingUtilities.invokeLater(() -> {
            new register().setVisible(true);
        });
        this.dispose(); // 关闭登录窗口
    }
    
    // 判斷ID是否存在
    private boolean isValidUserId(String userId) {
        UserRepository userRepository = new UserRepository();
        return userRepository.isUserIdExists(userId);
    }

